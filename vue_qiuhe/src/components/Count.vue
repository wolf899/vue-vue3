<template>
  <div>
    <h1>当前求和为：{{sum}}</h1>
    <select v-model="number">
      <option :value="1">1</option>
      <option :value="2">2</option>
      <option :value="3">3</option>
    </select>
    <button @click="addition">+</button>
    <button @click="subtraction">-</button>
    <button @click="additionOdd">当前求和为奇数再加</button>
    <button @click="additionWait">等0.5S再加</button>
  </div>
</template>

<script>
export default {
  name: 'Count',
  data(){
    return{
      number:1,
      sum:0,
    }
  },
  methods:{
    addition(){
      this.sum += this.number
    },
    subtraction(){
      this.sum -= this.number
    },
    additionOdd(){
      if(this.sum % 2){
        this.sum += this.number
      }
    },
    additionWait(){
      setTimeout(()=>{
        this.sum += this.number
      },500)
    }
  }
}
</script>

<style scoped>
  button{
    margin-left: 5px;
  }
</style>
